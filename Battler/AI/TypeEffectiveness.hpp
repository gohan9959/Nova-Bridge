struct TypeEffectiveness {
    // A simple pointer to the chosen generation's table
    const float (*currentTable)[19];

    // Constructor is just a pointer assignment (negligible cost)
    TypeEffectiveness(const float (*table)[19]) : currentTable(table) {}

    float getEffectiveness(Type attacker, Type defender) const {
        return currentTable[static_cast<size_t>(attacker)][static_cast<size_t>(defender)];
    }
};

